"use client"

import { useEffect, useState } from "react"
import { useRouter } from "next/navigation"
import { AdminLayout } from "@/components/admin-layout"

interface User {
  id: number
  gebruikersnaam: string
  email: string
  contactpersoon: string
  bedrijfsnaam: string
  rol: string
  status: string
}

